// program to demonstrate the usage of arithmetic operators (+,-,*,/,and%)
#include<stdio.h>

int main()
{
int m,n;
    printf("enter the first number\n");
    scanf("%d",&m);
    printf("enter the second number\n");
    scanf("%d",&n);
    printf("m+n = %d\n", m+n);
    printf("m-n = %d\n", m-n);
    printf("m/n = %d\n", m/n);
    printf("m*n = %d\n", m*n);
    printf("m%n = %d\n", m%n);
    return 0;
}